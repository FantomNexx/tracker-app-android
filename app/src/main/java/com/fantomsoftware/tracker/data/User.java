package com.fantomsoftware.tracker.data;

public class User{
   //---------------------------------------------------------------------------
   public int    id;
   public String name;
   //---------------------------------------------------------------------------


   //---------------------------------------------------------------------------
   public User( User user ){
      this.id = user.id;
      this.name = user.name;
   }//User
   //---------------------------------------------------------------------------
   public User(){
      this.id = -1;
      this.name = "";

   }//User
   //---------------------------------------------------------------------------

}//User
